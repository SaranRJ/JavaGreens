package org.india;

public class India {

	public void india() {
		System.out.println("Languages in India");
	}

	public void tamilLanguage() {
		System.out.println("Tamil");
	}

	public void malayalam() {
		System.out.println("Malayalam");

	}

	public void telugu() {
		System.out.println("Telugu");
	}

	public static void main(String[] args) {

		India in = new India();
		in.india();
		in.tamilLanguage();
		in.malayalam();
		in.telugu();
	}

}
